import jsPDF from "jspdf";

/**
 * Adds legal disclaimer footer to every page of a jsPDF doc.
 * Call AFTER all content pages have been created.
 */
export function addDisclaimerToAllPages(doc: jsPDF) {
  const rtl = (text: string) => text.split("").reverse().join("");
  const totalPages = doc.getNumberOfPages();

  for (let i = 1; i <= totalPages; i++) {
    doc.setPage(i);
    const pageWidth = doc.internal.pageSize.getWidth();
    const pageHeight = doc.internal.pageSize.getHeight();

    // Separator line
    doc.setDrawColor(100, 100, 100);
    doc.setLineWidth(0.3);
    doc.line(15, pageHeight - 22, pageWidth - 15, pageHeight - 22);

    // Disclaimer text
    doc.setFontSize(5.5);
    doc.setTextColor(120, 120, 120);

    const disclaimerLines = doc.splitTextToSize(rtl(LEGAL_DISCLAIMER_HE), pageWidth - 40);
    disclaimerLines.forEach((line: string, idx: number) => {
      doc.text(line, pageWidth / 2, pageHeight - 18 + idx * 3, { align: "center" });
    });

    // Watermark hash
    doc.setFontSize(4.5);
    doc.setTextColor(80, 80, 80);
    const hash = `Chitumit-Verified | SHA: ${generateDocHash(doc, i)} | Page ${i}/${totalPages}`;
    doc.text(hash, pageWidth / 2, pageHeight - 5, { align: "center" });
  }
}

/**
 * Adds diagonal watermark on the current page
 */
export function addSecureWatermark(doc: jsPDF, text = "CHITUMIT VERIFIED") {
  const pageWidth = doc.internal.pageSize.getWidth();
  const pageHeight = doc.internal.pageSize.getHeight();

  doc.saveGraphicsState();
  // @ts-ignore — jsPDF GState
  doc.setGState(new (doc as any).GState({ opacity: 0.04 }));
  doc.setFontSize(60);
  doc.setTextColor(212, 175, 55);

  // Rotate and center
  const centerX = pageWidth / 2;
  const centerY = pageHeight / 2;
  doc.text(text, centerX, centerY, {
    align: "center",
    angle: 35,
  });
  doc.restoreGraphicsState();
}

function generateDocHash(_doc: jsPDF, page: number): string {
  const ts = Date.now().toString(36);
  const pg = page.toString(16).padStart(2, "0");
  return `${ts.slice(-6)}${pg}`.toUpperCase();
}
