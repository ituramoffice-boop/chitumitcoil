/* ─────────────────────────────────────────────────────────────
   3. AUDIT LOG — per-case "Black Box" timeline
   ───────────────────────────────────────────────────────────── */
interface AuditEntry {
  id: string;
  timestamp: string;
  action: string;
  detail: string;
  policyVersion?: string;
  actor: "system" | "user" | "ai";
}

function generateAuditEntries(lead: {
  id: string;
  full_name: string;
  status: string;
  created_at: string;
  mortgage_amount: number | null;
  property_value: number | null;
  monthly_income: number | null;
}): AuditEntry[] {
  const entries: AuditEntry[] = [];
  const created = new Date(lead.created_at);

  entries.push({
    id: `${lead.id}-1`,
    timestamp: created.toISOString(),
    action: "תיק נפתח",
    detail: `נוצר תיק עבור ${lead.full_name}`,
    actor: "user",
  });

  if (lead.mortgage_amount) {
    const t = new Date(created.getTime() + 60000);
    entries.push({
      id: `${lead.id}-2`,
      timestamp: t.toISOString(),
      action: "נתונים פיננסיים הוזנו",
      detail: `סכום משכנתא: ₪${Number(lead.mortgage_amount).toLocaleString()}`,
      actor: "user",
    });
  }

  // AI analysis
  const aiTime = new Date(created.getTime() + 120000);
  entries.push({
    id: `${lead.id}-3`,
    timestamp: aiTime.toISOString(),
    action: "ניתוח AI בוצע",
    detail: "סריקת חיתום אוטומטית — מדיניות אשראי v2.4",
    policyVersion: "BOI Credit Policy v2.4 (03/2026)",
    actor: "ai",
  });

  if (lead.property_value && lead.mortgage_amount) {
    const ltv = ((Number(lead.mortgage_amount) / Number(lead.property_value)) * 100).toFixed(1);
    entries.push({
      id: `${lead.id}-4`,
      timestamp: new Date(aiTime.getTime() + 30000).toISOString(),
      action: "בדיקת LTV",
      detail: `יחס LTV: ${ltv}% — ${Number(ltv) <= 75 ? "תקין" : "חריגה"}`,
      policyVersion: "BOI Directive 329 (LTV Limits)",
      actor: "ai",
    });
  }

  if (["submitted", "approved", "closed"].includes(lead.status)) {
    entries.push({
      id: `${lead.id}-5`,
      timestamp: new Date(aiTime.getTime() + 300000).toISOString(),
      action: "דוח חיתום הופק",
      detail: "Executive Dossier PDF נוצר ואומת",
      policyVersion: "Chitumit Underwriting Engine v2.0",
      actor: "system",
    });
  }

  if (lead.status === "approved") {
    entries.push({
      id: `${lead.id}-6`,
      timestamp: new Date(aiTime.getTime() + 600000).toISOString(),
      action: "אישור התקבל",
      detail: "התיק אושר — צילום מצב נשמר",
      actor: "system",
    });
  }

  return entries.sort((a, b) => new Date(b.timestamp).getTime() - new Date(a.timestamp).getTime());
}

const actorConfig = {
  system: { label: "מערכת", color: "text-primary", icon: Lock },
  user: { label: "משתמש", color: "text-gold", icon: FileText },
  ai: { label: "AI", color: "text-emerald-400", icon: ShieldCheck },
};

